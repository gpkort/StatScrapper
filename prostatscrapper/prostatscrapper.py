from enum import Enum
from string import ascii_lowercase
from bs4 import BeautifulSoup
import pandas as pd
import requests

class POSITION(Enum):
    """
    An enumerator to define player positions

    """
    NONE = 0,
    RUNNINGBACK = 1,
    QUARTERBACK = 2,
    WIDE_RECEIVER = 3,
    TIGHT_END = 4


STANDARD_URL = 'http://www.pro-football-reference.com'
PLAYERS = '/players'
SLASH = '/'


class ProStatScrapper(object):
    """
    ProStatScrapper

        This is a class for scrapping the Pro Football Reference website
        http://www.pro-football-reference.com

        v 1.0 contains functionality for gathering primary offensive positions

        """
    __PLAYER_DICTIONARY = {POSITION.RUNNINGBACK: 'RB',
                           POSITION.QUARTERBACK: 'QB',
                           POSITION.WIDE_RECEIVER: 'WR',
                           POSITION.TIGHT_END: 'TE'}

    __rb_dataset = None

    def __init__(self, path=None):
        """
        Constructor
        :param path: Optional parameter to override default url for pro football site
        """
        self.url = STANDARD_URL if path is None else path

    def get_all(self):
        """
        allows users to scrape all players for all values of the Position enum

        :return:
        a dictionary {position enum, array of strings of column names}

        """
        all_dict = {}
        for pos in POSITION:
            if pos == POSITION.NONE:
                continue
            else:
                all_dict[pos] = self.get_all_players_by_position(pos)

        return all_dict

    def get_all_columns(self):
        """
        get_all_columns method allows user to inspect the column names
        of the datasets for each position

        :return:
        a dictionary {position enum, array of strings of column names}

        """
        column_dict = {}

        for pos in POSITION:
            if pos == POSITION.NONE:
                continue
            else:
                column_dict[pos] = self.get_columns(pos)

        return column_dict

    def get_columns(self, position):
        """
        get_column allows user to inspect column names for a specific position

        :param position:
        Position Enum value of desired column array

        :return:
        An array of strings that are the column names for the specified Position
        """
        if position == POSITION.RUNNINGBACK:
            return ['id', 'name', 'team', 'age', 'year', 'g', 'gs', 'rush_att', 'rush_yds',
                    'rush_td', 'rush_long', 'rush_yds_per_att', 'rush_yds_per_g',
                    'rush_att_per_g', 'rec', 'rec_yds', 'rec_yds_per_rec', 'rec_td',
                    'rec_long', 'rec_per_g', 'rec_yds_per_g', 'yds_from_scrimmage',
                    'rush_receive_td', 'fumbles']

        elif position == POSITION.QUARTERBACK:
            return ['id', 'name', 'team', 'age', 'year', 'g', 'gs', 'pass_cmp', 'pass_att',
                    'pass_cmp_perc', 'pass_yds', 'pass_td', 'pass_td_perc', 'pass_int',
                    'pass_int_perc', 'pass_long', 'pass_yds_per_att', 'pass_adj_yds_per_att',
                    'pass_yds_per_cmp', 'pass_yds_per_g', 'pass_rating', 'pass_sacked',
                    'pass_sacked_yds', 'pass_net_yds_per_att', 'pass_adj_net_yds_per_att',
                    'pass_sacked_perc']

        elif position == POSITION.WIDE_RECEIVER or position == POSITION.TIGHT_END:
            return ['id', 'name', 'age', 'year', 'team', 'g', 'gs', 'rec', 'rec_yds',
                    'rec_yds_per_rec', 'rec_td', 'rec_long', 'rec_per_g', 'rec_yds_per_g',
                    'catch_pct', 'rush_att', 'rush_yds', 'rush_td', 'rush_long',
                    'rush_yds_per_att', 'rush_yds_per_g', 'rush_att_per_g', 'yds_from_scrimmage',
                    'rush_receive_td', 'fumbles']
        else:
            return None

    def get_players(self, position, letter):
        """
        Allows user to scrape players for a given Position and last initial

        :param position:
        Position Enum value of desired column array

        :param letter:
        First letter of the players last name, case insensitive. If a string
        with more than one letter is passed, everything but the first letter
        is ignored.

        :return:
        A collection of data frames for all the years of all the players
        """
        roster = self.get_players_dataset(position, letter)
        pdf = None

        if len(roster):
            if position == POSITION.RUNNINGBACK:
                pdf = self.__get_runningbacks_dataset(roster)
            elif position == POSITION.QUARTERBACK:
                pdf = self.__get_quarterbacks_dataset(roster)
            elif position == POSITION.TIGHT_END or position == POSITION.WIDE_RECEIVER:
                pdf = self.__get_catching_dataset(roster, position)

        return pdf


    def get_all_players_by_position(self, position):
        """
        get_all_player_by_position method allows user to get
        an array of all the players of a given position

        :param position:
        Position Enum value of desired column array

        :return:
        A list of lists of all playes by initial of the players last name
        """
        ds_list = []

        for letter in ascii_lowercase:
            player_df = self.get_players_dataset(position, letter)
            print('Getting th players that begin with ' + letter)

            if position == POSITION.RUNNINGBACK:
                ds_list.append(self.__get_runningbacks_dataset(player_df))
            elif position == POSITION.QUARTERBACK:
                ds_list.append(self.__get_quarterbacks_dataset(player_df))
            elif position == POSITION.TIGHT_END or position == POSITION.WIDE_RECEIVER:
                ds_list.append(self.__get_catching_dataset(player_df, position))

        return pd.concat(ds_list)

    def get_players_dataset(self, position, letter):
        """
        Allows user to scrape players for a given Position and last initial

        :param position:
        Position Enum value of desired column array

        :param letter:
        First letter of the players last name, case insensitive. If a string
        with more than one letter is passed, everything but the first letter
        is ignored.

        :return:
        A list of lists of all playes by initial of the players last name
        """
        req = requests.get(STANDARD_URL + '/' + PLAYERS + '/' + letter.upper()[:1])
        soup = BeautifulSoup(req.text, "lxml")
        players = soup.find_all('div', {'class': 'section_content'})
        player_df = pd.DataFrame(columns=('link', 'name'))
        i = 0
        pos = '(' + ProStatScrapper.__PLAYER_DICTIONARY[position] + ')'

        for div in players:
            for line in div.find_all('p'):
                if pos in line.text:
                    link = line.find('a')
                    player_df.loc[i] = [str(link['href']), link.text]
                    i += 1

        return player_df

    def __get_quarterbacks_dataset(self, qb_dataset):
        i = 0
        years = pd.DataFrame(columns=self.get_columns(POSITION.QUARTERBACK))

        for prow in qb_dataset.iterrows():
            print('Retrieving quarterback ' + prow[1]['name'])

            url = STANDARD_URL + prow[1]['link']
            req = requests.get(url)
            soup = BeautifulSoup(req.text, "lxml")
            stats_overall = soup.find_all('table', {'class': 'row_summable sortable stats_table'})
            if len(stats_overall) == 0:
                continue

            stats_table = stats_overall[0].find_all('tbody')
            if len(stats_table) == 0:
                continue

            last_slash_idx = url.rfind('/') + 1
            last_dot_idx = url.rfind('.')
            pid = url[last_slash_idx: last_dot_idx]

            for row in stats_table[0].find_all('tr'):
                if row.has_attr('id') and 'passing' in row['id']:
                    year = row.find('th').text[:4]
                    g = row.find('td', {'data-stat': 'g'}).text
                    gs = row.find('td', {'data-stat': 'gs'}).text
                    pass_cmp = row.find('td', {'data-stat': 'pass_cmp'}).text
                    pass_att = row.find('td', {'data-stat': 'pass_att'}).text
                    pass_cmp_perc = row.find('td', {'data-stat': 'pass_cmp_perc'}).text
                    pass_yds = row.find('td', {'data-stat': 'pass_yds'}).text
                    pass_td = row.find('td', {'data-stat': 'pass_td'}).text
                    pass_td_perc = row.find('td', {'data-stat': 'pass_td_perc'}).text
                    pass_int_perc = row.find('td', {'data-stat': 'pass_int_perc'}).text
                    pass_int = row.find('td', {'data-stat': 'pass_int'}).text
                    pass_long = row.find('td', {'data-stat': 'pass_long'}).text
                    pass_yds_per_att =\
                        row.find('td', {'data-stat': 'pass_yds_per_att'}).text
                    pass_adj_yds_per_att = \
                        row.find('td', {'data-stat': 'pass_adj_yds_per_att'}).text
                    pass_yds_per_cmp = \
                        row.find('td', {'data-stat': 'pass_yds_per_cmp'}).text
                    pass_yds_per_g = row.find('td', {'data-stat': 'pass_yds_per_g'}).text
                    pass_rating = row.find('td', {'data-stat': 'pass_rating'}).text
                    pass_sacked = row.find('td', {'data-stat': 'pass_sacked'}).text
                    pass_sacked_yds = row.find('td', {'data-stat': 'pass_sacked_yds'}).text
                    pass_net_yds_per_att = \
                        row.find('td', {'data-stat': 'pass_net_yds_per_att'}).text
                    pass_adj_net_yds_per_att = \
                        row.find('td', {'data-stat': 'pass_adj_net_yds_per_att'}).text
                    pass_sacked_perc = row.find('td', {'data-stat': 'pass_sacked_perc'}).text
                    age = row.find('td', {'data-stat': 'age'}).text
                    team = row.find('td', {'data-stat': 'team'}).text

                    years.loc[i] = [pid, prow['name'], team, age, year, g, gs, pass_cmp,
                                    pass_att, pass_cmp_perc, pass_yds, pass_td,
                                    pass_td_perc, pass_int, pass_int_perc, pass_long,
                                    pass_yds_per_att, pass_adj_yds_per_att,
                                    pass_yds_per_cmp, pass_yds_per_g, pass_rating, pass_sacked,
                                    pass_sacked_yds, pass_net_yds_per_att, pass_adj_net_yds_per_att,
                                    pass_sacked_perc]
                    i += 1

        return years

